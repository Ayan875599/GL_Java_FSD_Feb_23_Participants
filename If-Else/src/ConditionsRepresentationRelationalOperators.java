
public class ConditionsRepresentationRelationalOperators {

	public static void main(String[] args) {
		
		boolean condition = (30 < 80);		
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}
		
		condition = (80 > 30);		
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}
				
		condition = (3 >= 3);
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}

		condition = (5 <= 10);
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}

		condition = (3 == 10);
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}

		condition = (10 != 10);
		if (condition) {
			System.out.println("Value of condition is  " + condition);			
		}
	
	}
}
